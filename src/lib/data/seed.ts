function creator(
  partial: Omit<CreatorProfile, "wallets"> & {
    wallets?: CreatorProfile["wallets"];
  },
): CreatorProfile {
  return {
    wallets: partial.wallets ?? [
      { chain: "evm", address: `0x${partial.id.padStart(40, "0")}` },
    ],
    ...partial,
  };
}

